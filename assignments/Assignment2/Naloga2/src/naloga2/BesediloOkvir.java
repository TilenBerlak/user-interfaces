    private void OdpriCB(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OdpriCB
       
        if(jfc.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File f = jfc.getSelectedFile();
            jLabel4.setText("Datoteka: " + f.getName());
            try {
                jTextPane1.read(new FileReader(f), null);
                jLabelStatus.setText("Odprta datoteka: " + f.getName() + "  " + f.length() + "B");
            } catch (Exception e) {
                jLabelStatus.setText("Napaka pri branju datoteke: " + f.getName());
            }
  
        }
        
    }//GEN-LAST:event_OdpriCB

