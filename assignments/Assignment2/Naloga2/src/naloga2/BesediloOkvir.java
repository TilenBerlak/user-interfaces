    private void ShraniCB(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ShraniCB
        
        if(jfc.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
            File f = jfc.getSelectedFile();
            jLabel4.setText("Datoteka: " + f.getName());
            try {
                jTextPane1.write(new FileWriter(f));
                jLabelStatus.setText("Datoteka je shranjena");
            } catch (Exception e) {
                jLabelStatus.setText("Napaka pri shranjevanju datoteke: " + f.getName());
            }
        }    
    }//GEN-LAST:event_ShraniCB

    private void barvajBesediloCB(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_barvajBesediloCB
        
        Color c = JColorChooser.showDialog(this, "Izberi barvo ospredja", Color.yellow);
        StyledDocument d = jTextPane1.getStyledDocument();
        Style s = jTextPane1.addStyle("barva ospredja", null);
        StyleConstants.setForeground(s, c);
        
        d.setCharacterAttributes(jTextPane1.getSelectionStart(), jTextPane1.getSelectionEnd() - jTextPane1.getSelectionStart(), s, false);
        
    }//GEN-LAST:event_barvajBesediloCB

