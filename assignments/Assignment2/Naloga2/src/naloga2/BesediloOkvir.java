    private void najdiCB(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_najdiCB
        
        String iskanje = jTextFieldIskanje.getText();
        
        int i = jTextPane1.getText().indexOf(iskanje);
        if(i == -1) {
            jLabelStatus.setText("Iskani niz ne obstaja");       
        }
           
        i = jTextPane1.getText().indexOf(iskanje);
        jTextPane1.select(i, i+iskanje.length());

        StyledDocument d = jTextPane1.getStyledDocument();
        Style s = jTextPane1.addStyle("barva ozadja", null);
        StyleConstants.setBackground(s, Color.yellow);

        d.setCharacterAttributes(jTextPane1.getSelectionStart(), jTextPane1.getSelectionEnd() - jTextPane1.getSelectionStart(), s, false);
        jLabelStatus.setText("Iskani niz najden");
       
     
    }//GEN-LAST:event_najdiCB

    private void zamenjajCB(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_zamenjajCB
        String iskanje = jTextFieldIskanje.getText();
        
        int i = jTextPane1.getText().indexOf(iskanje);
        if(i == -1) {
            jLabelStatus.setText("Iskani niz ne obstaja");       
        }
        
        while(i != -1) {
            i = jTextPane1.getText().indexOf(iskanje);
            jTextPane1.select(i, i+iskanje.length());
            jTextPane1.replaceSelection(jTextFieldZamenjaj.getText());
            jLabelStatus.setText("Iskani niz zamenjan");
        }

    }//GEN-LAST:event_zamenjajCB

